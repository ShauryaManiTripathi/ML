import numpy as np

# Define the array
array = np.array([[1, 6, 7, 9], [7, 9, 3, 5]])

# Dimension of the array
dimensions = array.ndim
print(f"Dimensions of the array: {dimensions}")

# Shape of the array
shape = array.shape
print(f"Shape of the array: {shape}")

# Size of the array
size = array.size
print(f"Size of the array: {size}")
